package VehicleProject;

public class Boat extends Vehicle{
	public Boat(){
		System.out.println("A boat has been created.");
	}
	public void Starts(){
		System.out.println("A boat starts.");
	}
	public void Stops(){
		System.out.println("A boat stops.");
	}
	public void Accelerate(){
		System.out.println("A boat accelerates.");
	}
	public void Float(){
		System.out.println("A boat floats.");
	}
	public void Function(){
		System.out.println("A boat can carry 75 passengers.");
	}

}
